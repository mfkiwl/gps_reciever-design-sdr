	read_samples(data, data_len, argc==2? argv[1] : "raw_Jan12_2308.txt");

	for(i = 0; i < (sizeof SV / sizeof *SV); ++i)
		signals[i] = check_satellite(sample_freq, data, data_len, i);

	printf("# SV, S/N ratio, doppler shift (Hz), phase (chips)\n");
